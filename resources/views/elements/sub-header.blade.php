                var hash = this.hash || location.hash;
                hash.length > 1 && setTimeout(function () {
                    window.scrollBy(0, hash === "#top" && -(header.height() + 5));
                }, 50);
            },
            unminimize = function () {
                header.removeClass("min");
            },
